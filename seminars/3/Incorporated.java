

public class Incorporated {
    String corpName;
    String corpDept;

    public void incorporated(String CorpName, String CorpDept){
        this.corpName = CorpName;
        this.corpName = CorpDept;
    }

    public String getCorpName() {
        return corpName;
    }

    public String getCorpDept() {
        return corpName;
    }
}